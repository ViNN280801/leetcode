#include <array>
#include <string>

class Solution
{
private:
    static constexpr size_t s_size{18ul};
    static inline std::array<std::pair<int, std::string>, s_size> s_roman_nums{
        std::make_pair(1000, "M"),
        std::make_pair(900, "CM"),
        std::make_pair(500, "D"),
        std::make_pair(400, "CD"),
        std::make_pair(100, "C"),
        std::make_pair(90, "XC"),
        std::make_pair(50, "L"),
        std::make_pair(40, "XL"),
        std::make_pair(10, "X"),
        std::make_pair(9, "IX"),
        std::make_pair(8, "VIII"),
        std::make_pair(7, "VII"),
        std::make_pair(6, "VI"),
        std::make_pair(5, "V"),
        std::make_pair(4, "IV"),
        std::make_pair(3, "III"),
        std::make_pair(2, "II"),
        std::make_pair(1, "I")};

public:
    std::string intToRoman(int num)
    {
        /* === Edge cases === */
        // Roman num can't be 0 or negative
        if (num <= 0)
            return "";
        // Time complexity: O(1)
        // Space complexity: O(1)
        /* ================== */

        /* === Main logic === */
        /*
        We need to make this loop vice versa
        while num > 0:
            for _ in range(num // val[i]):
                roman_num += syms[i]
                num -= val[i]
            i += 1
        return roman_num
        */
        std::string roman_num;
        for (auto const &[key, value] : s_roman_nums)
        {
            while (num >= key)
            {
                roman_num += value;
                num -= key;

                // For example: 2541
                // 2541 - 1000 = 1541 -> M
                // 1541 - 1000 = 541 -> MM
                // 541 - 500 = 41 -> MMD
                // 41 - 40 = 1 -> MMDXL
                // 1 - 1 = 0 -> MMDXLI
            }
        }
        return roman_num;
        // Time complexity:
        // Worst case: 3999 -> O(18) -> O(1)
        // Best case: 1 -> O(1)
        // Space complexity: O(1)
        /* ================== */
    }
};

